print("-----Else-if Loop-------")
x = 20
if x > 10:
    print("Greater than 10")
elif x == 10:
    print("Equal to 10")
else:
    print("Less than 10")
print("-----For Loop-------")
# For loop
for counter in range(6):  #n-1 
    print(counter)  # Output: 0 1 2 3 4 5
print("-----While Loop-------")
# While loop
count = 1
while count < 5:
    print(count)
    count += 1  # Output: 1 2 3 4 
print("----END-----")